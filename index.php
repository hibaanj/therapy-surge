                <h2 class="section-heading text-uppercase">FAQ</h2>
                <p style="text-align: left"><strong>What is Therapy?</strong></br>
Therapy is a safe space where individuals can work through their mental health concerns. According to American Psychological Association " Psychologists apply research-based techniques to help people develop more effective habits. There are several approaches to psychotherapy, including cognitive-behavioral, interpersonal and psychodynamic, among others, that help people work through their problems".</br>
<strong>What sort of issues can therapy help with?</strong></br>
Therapy can be helpful for a broad range of concerns. Some of the common reasons people seek therapy are: depression or low mood, stress and anxiety, anger management, fears and phobias, trauma, low self-esteem, self-harm, obsessive behaviors, unhealthy relationships, abuse and many more concerns.</br>
<strong>Can I get a refund if I am not satisfied with the therapy?</strong></br>
Currently, we are not offering refunds on the packages purchased. However, we would assign you a different therapist to make sure you receive the best services.</br>
<strong>Do you offer sliding scale?</strong></br>
We do offer sliding scale for students and people who can't afford therapy. Reach out to us for more details.</br>
<strong>Is the information I share safe?</strong></br>
Yes! We ensure none of your information is shared with anyone but your Therapist . All the information you share with us is completely safe with us.
